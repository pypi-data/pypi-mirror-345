# -*- coding: utf-8 -*-

def kg_para_g(kg):
    return kg * 1000

def g_para_kg(g):
    return g / 1000

def lb_para_kg(lb):
    return lb * 0.45359237

def kg_para_lb(kg):
    return kg / 0.45359237

def kg_para_arroba(quilogramas):
    return quilogramas / 15.0

def arroba_para_kg(arrobas):
    return arrobas * 15.0




