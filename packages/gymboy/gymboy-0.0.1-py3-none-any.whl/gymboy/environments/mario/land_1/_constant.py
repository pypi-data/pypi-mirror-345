# Memory ADRs:
# https://datacrystal.tcrf.net/wiki/Super_Mario_Land/RAM_map


# (3 Bytes) Score
SCORE_ADDRESS = 0xC0A0

# (1 Byte) World Level address
WORLD_LEVEL_ADDRESS = 0xFFB4

# (2 Bytes) Coins
COINS_ADDRESS = 0x9829

# (1 Byte) Lives left
LIVES_ADDRESS = 0xDA15

# (3 Bytes) Timer left
TIMES_ADDRESS = 0x9831

# (1 Byte) Time up Flag
TIME_UP_ADDRESS = 0xDA1D

# (1 Byte) Game over Flag
GAME_OVER_ADDRESS = 0xC0A4

# (1 Byte) Level complete Flag
LEVEL_COMPLETE = 0xFFB3
